module imem (
    input  logic [31:0] addr,
    output logic [31:0] instr
);
    logic [31:0] mem [0:255];

    initial begin
        // ------------------------------------------------------------
        // Arithmetic / logic setup
        // ------------------------------------------------------------
        mem[0]  = 32'h20010005; // addi $1,  $0, 5      ; r1  = 5
        mem[1]  = 32'h2002000a; // addi $2,  $0, 10     ; r2  = 10
        mem[2]  = 32'h00221820; // add  $3,  $1, $2     ; r3  = 15
        mem[3]  = 32'h00412022; // sub  $4,  $2, $1     ; r4  = 5
        mem[4]  = 32'h00222824; // and  $5,  $1, $2     ; r5  = 0
        mem[5]  = 32'h00223025; // or   $6,  $1, $2     ; r6  = 15

        // ------------------------------------------------------------
        // Immediate operations
        // ------------------------------------------------------------
        mem[6]  = 32'h30c70003; // andi $7,  $6, 3      ; r7  = 3
        mem[7]  = 32'h34a8000c; // ori  $8,  $5, 12     ; r8  = 12
        mem[8]  = 32'h20090064; // addi $9,  $0, 100    ; r9  = 100

        // ------------------------------------------------------------
        // Stores
        // ------------------------------------------------------------
        mem[9]  = 32'hac030000; // sw   $3,  0($0)      ; mem[0] = 15
        mem[10] = 32'hac040004; // sw   $4,  4($0)      ; mem[1] = 5
        mem[11] = 32'hac060008; // sw   $6,  8($0)      ; mem[2] = 15

        // ------------------------------------------------------------
        // Loads + dependent arithmetic
        // ------------------------------------------------------------
        mem[12] = 32'h8c0a0000; // lw   $10, 0($0)      ; r10 = 15
        mem[13] = 32'h8c0b0004; // lw   $11, 4($0)      ; r11 = 5
        mem[14] = 32'h014b6020; // add  $12, $10, $11   ; r12 = 20

        // ------------------------------------------------------------
        // Branch not taken
        // beq r12,r8,+1 : 20 == 12? no, so next instr executes
        // ------------------------------------------------------------
        mem[15] = 32'h11880001; // beq  $12, $8, +1
        mem[16] = 32'h200d0001; // addi $13, $0, 1      ; executes

        // ------------------------------------------------------------
        // Branch taken
        // bne r12,r8,+1 : 20 != 12? yes, skip mem[18]
        // ------------------------------------------------------------
        mem[17] = 32'h15880001; // bne  $12, $8, +1
        mem[18] = 32'h200e0063; // addi $14, $0, 99     ; skipped
        mem[19] = 32'h200f0002; // addi $15, $0, 2      ; executes

        // ------------------------------------------------------------
        // Jump
        // j 22 : skip mem[21]
        // ------------------------------------------------------------
        mem[20] = 32'h08000016; // j    22
        mem[21] = 32'h20100058; // addi $16, $0, 88     ; skipped
        mem[22] = 32'h20110007; // addi $17, $0, 7      ; executes

        // ------------------------------------------------------------
        // More memory + arithmetic
        // ------------------------------------------------------------
        mem[23] = 32'hac0c000c; // sw   $12, 12($0)     ; mem[3] = 20
        mem[24] = 32'h8c12000c; // lw   $18, 12($0)     ; r18 = 20
        mem[25] = 32'h02519820; // add  $19, $18, $17   ; r19 = 27

        // ------------------------------------------------------------
        // Final safe loop
        // ------------------------------------------------------------
        mem[26] = 32'h1000ffff; // beq  $0, $0, -1      ; loop forever

        // Fill remaining memory with NOPs
        for (int i = 27; i < 256; i++) begin
            mem[i] = 32'h00000000;
        end
    end

    assign instr = mem[addr[9:2]]; // word addressed

endmodule
